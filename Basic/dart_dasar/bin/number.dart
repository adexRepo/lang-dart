// * Keterangan :
// * - Number adalah tipe data angka, terdapat dua jenis tipe data Number, yaitu :
// *   - Integer (int)
// *   - double (float)
// * - integer adalah tipe data bilangan bulat,
// * - double adalah tipe data bilangan desimal,
// * - Penulisan koma dalam double menggunakan titik (.), bukan koma (,)
// * - Jadi jika kita akan membuat bilangan desimal 0,5 (nol koma lima), maka ditulis 0.5

void main() {
  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  /* 
    TIPE DATA NUM
      - Jika kita ingin menggunakan tipe data number, yang bisa int ataupun double,
      maka kita harus menggunakan tipe data num, yaitu tipe data num.
   */

  num number3 = 10;
  print(number3);

  number3 = 10.5;

  print(number3); //10.5
}
