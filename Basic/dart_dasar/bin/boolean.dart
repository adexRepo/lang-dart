// * Keterangan :
// * - Boolean adalah tipe data yang hanya memiliki dua nilai yaitu benar dan salah
// * - Boolean di representasikan dengan kata kunci bool
// * - Untuk nilai benar, menggunakan kata kunci true
// * - Untuk nilai salah, menggunakan kata kunci false

void main() {
  bool isTrue = true;
  bool isFalse = false;

  print(isTrue); //true
  print(isFalse); //false
}
