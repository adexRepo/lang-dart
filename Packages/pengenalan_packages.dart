/* 
  ANCHOR Introduction to Dart Packages
  - Ekosistem Dart menggunakan packages untuk melakukan management software
  yang bisa di sharing, seperti library atau tools.
  - Saat kita membuat project di Dart, sebenarnya secara tidak langsung kita
  membuat packages.
  - Packages bisa dalam bentuk aplikasi atau library (yang digunakan pada aplikasi).
  - Uniknya di dart ini dia bisa membuat library sendiri tidak seperti java dkk.
  library ini bisa digunakan didalam project kita sendiri atau bisa digunakan
  di project lainnya.

  ANCHOR Keuntungan :
  - Dengan menggunakan packages, kita akan mengikuti standart cara management kode Dart
  - Dengan packages juga kita bisa menggunakan dependency management secara otomatis
  tanpa harus download library-library yang kita butuhkan secara manual.
 */