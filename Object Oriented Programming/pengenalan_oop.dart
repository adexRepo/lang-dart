// * Apa itu Dart Object Oriented Programming
// Object oriented programming adalah sudut pandang bahasa pemrograman yang berkonsep "Object"
// Ada banyak sudut pandang bahasa pemrograman namun, OOP adalah yang sangat populer saat ini.
// Ada beberapa istilah yang perlu dimengerti dari OOP, yaitu Object dan Class.


// * Object
// Object adalah sebuah kumpulan data dan metode yang dapat digunakan untuk mengolah data tersebut.
// Object ialah data yang berisi field / property / attribute dan method / function / behavior.
// SEMUA TIPE DATA di DART adalah Object

// * Class
// Class adalah sebuah template yang digunakan untuk membuat object.
// Class dapat memiliki property dan function yang dimiliki oleh object.
// Object selalu dibuat didalam class